let character = 'f'; // initialize 'f' character
let timesToRepeat = 5; // initialize number of times to repeat the character 
let myString = ''; // declare an empty string to hold the character after each iteration of the for loop

for (let i = 0; i < timesToRepeat; i++) { // for loop to iterate the number of times the character is to be repeated

    myString += character; // set the value of myString equal to the value of character after each iteration

}

console.log(myString); // print the value of myString after the for loop is complete

// for loops are great for defined times to run a loop 